以下のいずれかの方法で登録：
- Googleアカウントで登録（推奨：最速）
- Microsoftアカウントで登録
- メールアドレスで登録

Googleアカウント連携を選ぶと、メール認証なしで即座に使い始められます。

**ステップ3: 電話番号認証**
1. 携帯電話番号を入力（日本の場合は +81 を選択）
2. SMSで届いた6桁のコードを入力

※1つの電話番号で1アカウントのみ作成可能

**ステップ4: 完了！**
すぐに ChatGPT の画面が表示され、使い始められます。

### インターフェースの見方

ChatGPTの画面は非常にシンプルです：

**左サイドバー**:
- 「New chat」ボタン：新しい会話を開始
- 過去の会話履歴：クリックで再開可能
- 設定アイコン：アカウント情報、カスタム指示など

**中央エリア**:
- メッセージ入力欄：ここに質問やお願いを書く
- 会話履歴：やり取りが時系列で表示される

**メッセージの操作**:
- コピーボタン：回答をクリップボードにコピー
- 再生成ボタン：同じ質問で別の回答を生成
- 良い評価/悪い評価：OpenAIへのフィードバック

### 基本的な使い方

**会話の開始**:
1. 下部の入力欄に質問やお願いを書く
2. Enterキー（またはWindows: Ctrl+Enter, Mac: Command+Enter）で送信
3. 数秒で回答が表示される

**会話の継続**:
- 前の回答を踏まえて追加質問ができる
- 「もっと詳しく」「別の例を教えて」などと言えば深掘り可能
- 1つの会話内では文脈が維持される

**新しい話題**:
- 全く別の話題に移るときは「New chat」で新規会話を開始
- 前の会話の文脈を引きずらないため

